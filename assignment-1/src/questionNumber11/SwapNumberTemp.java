package questionNumber11;

public class SwapNumberTemp {

	public static void main(String[] args) {
		int x=1;
	      int y=2;
	      int temp;
	      System.out.println("Value before swap -> x = " +x+" and y = "+y);
	      temp=x;
	      x=y;
	      y=temp;
	      System.out.println("Value after swap -> x = " +x+" and y = "+y);	

	}

}
