package questionNumber1;

public class DispGreetings3 {

	public static void main(String[] args) {
		for(int i=1; i<=3; i++) {
			if(i==1) {
				System.out.println("Hello");
			}
			else if(i==2) {
				System.out.println("Good Morning");
			}
			else {
				System.out.println("How have you been?");
			}
		}

	}

}
