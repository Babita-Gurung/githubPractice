package iteration.statement;

public class ForLoopDemo {

	public static void main(String[] args) {
		// for loop
		int count=0;
		for(int i=0;i<5;i++) {
			System.out.println("Hello World ");
			count++;
			
		}
		System.out.println("------------------------------------------------------");
		System.out.println("Total number of above statement printed: "+count);

	}

}
