
public class ArithmeticOpDemo {

	public static void main(String[] args) {
		//Program that demonstrate the arithmetic operators 
		//Arithmetic operators (+,-,/,*,%) 
		int num1=20;
		int num2=3;
		int add=num1+num2;
		int sub=num1-num2;
		int mul=num1*num2;
		float div=(float)num1/num2;
		int mod=num1%num2;
		
		System.out.println("Here num1: 20 and num2: 3");
		System.out.println("");
		System.out.println("Sum of num1 and num2 is: " + add);
		System.out.println("------------------------------------------------------");
		System.out.println("Difference of num2 from num2 is: " + sub);
		System.out.println("------------------------------------------------------");
		System.out.println("Multiplication of num1 and num2 is: " + mul);
		System.out.println("------------------------------------------------------");
		System.out.println("The division of num1 from num2 is: " + div);
		System.out.println("------------------------------------------------------");
		System.out.println("Modulous of num1 and num2 is: " + mod);
		System.out.println("------------------------------------------------------");

	}

}
